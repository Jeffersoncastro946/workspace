    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            // TODO add your handling code here:
            ps=  cx.conectar().prepareStatement("SELECT * FROM persona WHERE clave= ?" );
            ps.setString(1, txtClave.getText());
            //USAR RES PARA MOSTRAR LOS RESULTADOS
            res=ps.executeQuery();
            if(res.next()){
               txtId.setText(res.getString("idpersona"));
               txtNombre.setText(res.getString("nombre"));
               txtDireccion.setText(res.getString("direccion"));
               txtTelefono.setText(res.getString("telefono"));
               txtEmail.setText(res.getString("correo_electronico"));
               txtDate.setText(res.getString("fecha_nacimiento"));
               cmbGenero.setSelectedItem(res.getString("genero"));
            }
            else{
                JOptionPane.showMessageDialog(null, "No existe ese registro");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Ventana1.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

