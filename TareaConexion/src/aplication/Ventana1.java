    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        
        try {
            
            ps=  cx.conectar().prepareStatement("UPDATE persona SET clave=?, nombre=?, direccion=?, telefono=?, correo_electronico=?, fecha_nacimiento=?, genero=? WHERE idpersona=? " );
            ps.setString(1, txtClave.getText());
            ps.setString(2, txtNombre.getText());
            ps.setString(3, txtDireccion.getText());
            ps.setString(4, txtTelefono.getText());
            ps.setString(5, txtEmail.getText());
            ps.setDate(6, Date.valueOf(txtDate.getText()));
            ps.setString(7, cmbGenero.getSelectedItem().toString());
              ps.setString(8, txtId.getText());
            int res =ps.executeUpdate();
            if(res>0){
                JOptionPane.showMessageDialog(null, "Guardado correctamente");
                limpiarCajas();
            }
            else{
                JOptionPane.showMessageDialog(null, "Guardado incorrectamente");
            }
            cx.deconectar();
            } catch (SQLException ex) {
            Logger.getLogger(Ventana1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

